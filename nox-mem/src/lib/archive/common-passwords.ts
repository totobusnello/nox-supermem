/**
 * Returns true when `lowercased` matches a common password entry directly.
 * Caller MUST pass already-lowercased string.
 */
export function isCommonPassword(lowercased: string): boolean {
  return COMMON_PASSWORDS.has(lowercased);
}

/**
 * Returns true when `lowercased` CONTAINS a common substring of length >=4.
 * Heuristic — used to penalise "Password!23" → contains "password".
 *
 * Note: we restrict to substrings of length >=4 to avoid pathological cases
 * (e.g., "amor" inside "saudosamor" would trigger; that's acceptable for an
 * entropy *estimate*, since true random Brazilian-Portuguese is unlikely to
 * contain those exact 4-grams clustered).
 */
export function containsCommonSubstring(lowercased: string): boolean {
  if (lowercased.length < 4) return false;
  for (const pw of COMMON_PASSWORDS) {
    if (pw.length < 4) continue;
    if (lowercased.includes(pw)) return true;
  }
  return false;
}
